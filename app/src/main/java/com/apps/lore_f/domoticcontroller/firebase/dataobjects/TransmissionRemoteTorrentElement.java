package com.apps.lore_f.domoticcontroller.firebase.dataobjects;

public class TransmissionRemoteTorrentElement {

    private String done;
    private float down;
    private float up;
    private String eta;
    private String have;
    private String id;
    private String name;
    private String ratio;
    private String status;

    public TransmissionRemoteTorrentElement() {};

    public String getDone() {
        return done;
    }

    public float getDown() {
        return down;
    }

    public float getUp() {
        return up;
    }

    public String getEta() {
        return eta;
    }

    public String getHave() {
        return have;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getRatio() {
        return ratio;
    }

    public String getStatus() {
        return status;
    }

}
